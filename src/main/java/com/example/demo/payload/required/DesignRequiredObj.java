package com.example.demo.payload.required;

public class DesignRequiredObj {
	private String requiredTitle;
	private String requiredDesc;
	private String note;
	private String orderId;
	private String imageLink;
	private String customerId;
	public String getRequiredTitle() {
		return requiredTitle;
	}
	public void setRequiredTitle(String requiredTitle) {
		this.requiredTitle = requiredTitle;
	}
	public String getRequiredDesc() {
		return requiredDesc;
	}
	public void setRequiredDesc(String requiredDesc) {
		this.requiredDesc = requiredDesc;
	}
	public String getNote() {
		return note;
	}
	public void setNote(String note) {
		this.note = note;
	}
	public String getOrderId() {
		return orderId;
	}
	public void setOrderId(String orderId) {
		this.orderId = orderId;
	}
	public String getImageLink() {
		return imageLink;
	}
	public void setImageLink(String imageLink) {
		this.imageLink = imageLink;
	}
	public String getCustomerId() {
		return customerId;
	}
	public void setCustomerId(String customerId) {
		this.customerId = customerId;
	}
}
